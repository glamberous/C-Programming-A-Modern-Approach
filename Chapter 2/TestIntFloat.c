/* write a program that initializes a bunch of INT and FLOATS */
#include <stdio.h>

int main(void)
{
	int one, two, three;
	float four, five, six;
	printf("Int 1: %d\nInt 2: %d\nInt 3: %d\n", one, two, three);
	printf("Float 1: %.2f\nFloat 2: %.5f\nFloat 3: %.4f\n", four, five, six);	
	
	return 0;
}

