/* write a program that initializes a bunch of INT and FLOATS */
#include <stdio.h>

int main(void)
{
	int one, two, three;
	float four, five, six, seven, eight;
	printf("Int 1: %d\nInt 2: %d\nInt 3: %d\n", one, two, three);
	printf("Float 1: %.2f\nFloat 2: %.5f\nFloat 3: %.5f\nFloat 4: %.5f\nFloat 5: %.5f\n", four, five, six, seven, eight);	
	
	return 0;
}

