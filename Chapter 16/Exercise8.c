/* create a program that will convert total seconds into hour, min, second format */
// 26400 = 7:20am

#include <stdio.h>

struct color
{
	int red;
	int green;
	int blue;
};

int main(void)
{
	const struct color MAGENTA = {255, .blue = 255};

	printf("%d, %d, %d\n", MAGENTA.red, MAGENTA.green, MAGENTA.blue);
}
